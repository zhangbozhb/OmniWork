#!/usr/bin/env node
import { spawn } from "node:child_process";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const agentEntry = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "main.ts");
const child = spawn(process.execPath, ["--experimental-strip-types", agentEntry], {
  env: process.env,
  stdio: "inherit",
});

child.on("exit", (code, signal) => {
  if (signal) {
    process.kill(process.pid, signal);
    return;
  }
  process.exitCode = code ?? 1;
});
