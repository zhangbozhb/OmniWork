import { type JSX, useEffect, useState } from "react";
import { Alert, ScrollView, StyleSheet, Text, TextInput, View } from "react-native";

import type { PairingConfig } from "../../features/auth/types";
import { isValidSessionKey } from "../../features/auth/keyProof";
import { appConfig } from "../../app/appConfig";
import { Button, Card } from "../../ui/components";
import { colors, radii, spacing, typography } from "../../ui/theme";

export interface PairingScreenProps {
  errorMessage?: string;
  initialPairing?: PairingConfig;
  submitLabel?: string;
  onCancel?(): void;
  onPair(pairing: PairingConfig): void | Promise<void>;
}

export function PairingScreen({ errorMessage, initialPairing, submitLabel = "Pair Mac", onCancel, onPair }: PairingScreenProps): JSX.Element {
  const [relayUrl, setRelayUrl] = useState(initialPairing?.relayUrl ?? appConfig.defaultRelayUrl);
  const [deviceId, setDeviceId] = useState(initialPairing?.deviceId ?? "");
  const [key, setKey] = useState(initialPairing?.key ?? "");
  const [submitting, setSubmitting] = useState(false);

  useEffect(() => {
    setRelayUrl(initialPairing?.relayUrl ?? appConfig.defaultRelayUrl);
    setDeviceId(initialPairing?.deviceId ?? "");
    setKey(initialPairing?.key ?? "");
  }, [initialPairing]);

  async function submit(): Promise<void> {
    const trimmedKey = key.trim();
    if (!isValidSessionKey(trimmedKey)) {
      Alert.alert("Invalid key", "Enter the 32-character key generated by the Mac Agent.");
      return;
    }
    if (!relayUrl.trim() || !deviceId.trim()) {
      Alert.alert("Missing connection details", "Relay URL and device ID are required.");
      return;
    }

    setSubmitting(true);
    try {
      await onPair({
        relayUrl: relayUrl.trim(),
        deviceId: deviceId.trim(),
        key: trimmedKey,
      });
    } finally {
      setSubmitting(false);
    }
  }

  return (
    <ScrollView
      keyboardShouldPersistTaps="handled"
      contentContainerStyle={styles.screen}
    >
      <Card success style={styles.scanCard}>
        <Text style={styles.scanEyebrow}>Recommended</Text>
        <Text style={styles.scanTitle}>Scan the Mac Agent QR code</Text>
        <Text style={styles.scanText}>
          Open the system camera, scan the QR code printed in the Mac Agent
          terminal, then tap the OmniWork link to fill these fields and connect.
        </Text>
      </Card>

      <View style={styles.dividerRow}>
        <View style={styles.divider} />
        <Text style={styles.dividerText}>Manual fallback</Text>
        <View style={styles.divider} />
      </View>

      <Text style={styles.label}>Relay URL</Text>
      <TextInput
        autoCapitalize="none"
        autoCorrect={false}
        value={relayUrl}
        onChangeText={setRelayUrl}
        placeholder="wss://relay.company.example/mobile"
        placeholderTextColor="#66727c"
        style={styles.input}
      />

      <Text style={styles.label}>Mac Device ID</Text>
      <TextInput
        autoCapitalize="none"
        autoCorrect={false}
        value={deviceId}
        onChangeText={setDeviceId}
        placeholder="your-mac.local"
        placeholderTextColor="#66727c"
        style={styles.input}
      />

      <Text style={styles.label}>Temporary Key</Text>
      <TextInput
        autoCapitalize="none"
        autoCorrect={false}
        value={key}
        onChangeText={setKey}
        placeholder="32-character key"
        placeholderTextColor="#66727c"
        secureTextEntry
        style={styles.input}
      />

      {errorMessage ? <Text style={styles.error}>{errorMessage}</Text> : null}

      <Button
        disabled={submitting}
        style={styles.primaryButton}
        tone="primary"
        onPress={submit}
      >
        {submitting ? "Saving..." : submitLabel}
      </Button>
      {onCancel ? (
        <Button onPress={onCancel}>Cancel</Button>
      ) : null}
    </ScrollView>
  );
}

const styles = StyleSheet.create({
  screen: {
    flexGrow: 1,
    padding: spacing.xxl,
    gap: spacing.md,
  },
  scanCard: {
    padding: spacing.xl,
  },
  scanEyebrow: {
    color: colors.success,
    ...typography.eyebrow,
    letterSpacing: 0.7,
  },
  scanTitle: {
    color: colors.textPrimary,
    fontSize: 21,
    fontWeight: "800",
    marginTop: 6,
  },
  scanText: {
    color: colors.textSecondary,
    fontSize: 14,
    lineHeight: 20,
    marginTop: spacing.sm,
  },
  dividerRow: {
    flexDirection: "row",
    alignItems: "center",
    gap: spacing.md,
    marginVertical: 6,
  },
  divider: {
    flex: 1,
    height: StyleSheet.hairlineWidth,
    backgroundColor: colors.border,
  },
  dividerText: {
    color: colors.textDim,
    ...typography.eyebrow,
    letterSpacing: 0.5,
  },
  label: {
    color: colors.textSecondary,
    ...typography.label,
  },
  input: {
    minHeight: 48,
    borderColor: colors.border,
    borderWidth: 1,
    borderRadius: radii.sm,
    color: colors.textPrimary,
    paddingHorizontal: spacing.lg,
    backgroundColor: colors.surface,
  },
  primaryButton: {
    minHeight: 48,
    marginTop: spacing.lg,
  },
  primaryButtonText: {
    color: colors.successText,
    ...typography.action,
    fontSize: 16,
  },
  disabled: {
    opacity: 0.55,
  },
  secondaryButton: {
    minHeight: 44,
    borderRadius: radii.sm,
    alignItems: "center",
    justifyContent: "center",
    borderColor: colors.border,
    borderWidth: 1,
  },
  secondaryButtonText: {
    color: colors.textSecondary,
    fontWeight: "700",
  },
  error: {
    color: colors.danger,
    marginTop: spacing.xs,
  },
});
